/* Operadores */

// 1. Operadores aritméticos
console.log(1 + 1);
console.log(2 - 4);
console.log(10 * 10);
console.log(10 / 2);
console.log(10 % 2);

// 2. Operadores comparativos

//console.log(1 == "1"); // Muestra error.
console.log(1 == 1);
console.log(1 != 1);
console.log(1 < 1);
console.log(1 > 1);
console.log(1 <= 1);
console.log(1 >= 1);

// 3. Operadores lógicos

console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

console.log(!true);
console.log(!false);
